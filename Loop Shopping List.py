# To print all the items in the shopping we use:
shopping_list1 = ["Apple", "Mango", "Orange", "Lime", "Banana"]
for fruit in shopping_list1:
    print(fruit)
# (this will print all the fruits)

# To print an item or two from a list we use:
shopping_list2 = ["Apple", "Mango", "Orange", "Lime", "Banana", "Guava"]
for i in range(2):
    print(shopping_list2[i])


